#include "ft_printf.h"

void	ft_put_nbr(long i)
{
	if (i < 0 )
	{
		ft_put_char ('-');
		i *= -1;
	}
	if (i < 10)
		ft_put_char ((char)('0' + i));
	if (i > 9)
	{
		ft_put_nbr (i / 10);
		ft_put_char ((char)('0' + (i % 10)));
	}
}
